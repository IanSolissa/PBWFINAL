<p>1/3 Tonight at midnight, Pink Floyd will release a new song, 'Hey Hey Rise Up', which sees David Gilmour and Nick Mason joined by Guy Pratt & Nitin Sawhney, with an extraordinary vocal by Andriy Khlyvnyuk of Ukrainian band Boombox.Listen/download at https://t.co/i1l92D3AYU.</p> <br>

<p>— Pink Floyd (@pinkfloyd) April 7, 2022
“Saya memiliki platform besar yang telah saya kerjakan selama bertahun-tahun (Pink Floyd). Ini sangat sulit dan membuat frustrasi untuk melihat serangan yang luar biasa gila dan tidak adil, terutama dilakukan oleh negara besar terhadap negara yang merdeka, damai, dan demokratis,” tutur Pink Floyd dalam wawancaranya dengan Guardian dikutip pada Jumat (08/04/2022).</p> <br>

<p>Gilmour memang memiliki menantu asal Ukraina sehingga para cucunya juga dapat dikatakan setengah darah Ukraina. Karena itulah, perang yang terjadi turut membuatnya frustrasi. </p> <br>

